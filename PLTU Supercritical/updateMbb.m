function Mfuel = updateMbb(Loop,Mbb1,Mbb2,Mbb3,Mbb4,Mbb5)
    if Loop == 1,
        Mfuel = Mbb1;
        return;
    elseif Loop == 2,
        Mfuel = Mbb2;
        return;
    elseif Loop == 3,
        Mfuel = Mbb3;
        return;
    elseif Loop == 4,
        Mfuel = Mbb4;
        return;
    elseif Loop == 5,
        Mfuel = Mbb5;
        return;
    end
end